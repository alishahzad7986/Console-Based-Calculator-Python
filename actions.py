# addition
def add(value_1, value_2):
    value = value_1 + value_2
    return value

# subtract
def sub(value_1, value_2):
    value = value_1 - value_2
    return value

# multiplication
def mul(value_1, value_2):
    value = value_1 * value_2
    return value

# divide
def div(value_1, value_2):
    value = value_1 / value_2
    return value
#square
def squ(value_1):
    value = value_1 ** 2
    return value
# cube
def cub(value_1):
    value = value_1 ** 3
    return value
# exponent
def exp(value_1, value_2):
    value = value_1 ** value_2
    return value